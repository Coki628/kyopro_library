void solve() {
    ll N;
    cin >> N;
    auto A = LIST(N);

    vector<ll> A1, A2;
    rep(i, N) {
        if (A[i]%2) {
            A1.eb(A[i]);
        } else {
            A2.eb(A[i]);
        }
    }

    if (sorted(A1) == A1 and sorted(A2) == A2) {
        Yes();
    } else {
        No();
    }
}

